// Ternary operator:

// The ternary operator is a concise way to write an if-else statement that assigns a value to a variable based on a condition.

// Syntax: (condition) ? valueIfTrue : valueIfFalse

let x = 10;
let y = 20;
let max;

// Example:
// If x is greater than y, assign x to max; otherwise, assign y to max.
max = (x > y) ? x : y;

console.log(max); // Output: 20 (because y is greater than x)
