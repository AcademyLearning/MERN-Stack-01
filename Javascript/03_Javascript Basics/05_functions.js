// JavaScript functions are blocks of code that can be defined and executed whenever needed.

// Syntax for defining a function:
// function functionName() {
//     // code to be executed 
// }

// Example 1:
// Defines a function called square that calculates the square of a number.
function square(x) { // Function definition with a parameter 'x'
    console.log(x * x); // Calculates the square of 'x' and logs it to the console
    return x * x; // Returns the square of 'x'
}

let result = square(5); // Calls the square function with an argument of 5 and stores the result in the 'result' variable.

