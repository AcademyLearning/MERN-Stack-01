// Converting a string representation of a number to a numeric value using the Number function.
console.log("Converting string to number:", Number("3.14"));

// Assigning the numeric value obtained from converting a string to a variable.
var test = Number("3.14");

// Assigning a string representation of a number to a variable.
var test1 = "3.14";

// Checking the type of the variable storing the numeric value.
console.log("Type of test:", typeof(test)); // Output: "number"

// Checking the type of the variable storing the string.
console.log("Type of test1:", typeof(test1)); // Output: "string"
