// Generating a random number between 0 (inclusive) and 1 (exclusive).
console.log("Random number:", Math.random());

// Rounding down a decimal number to the nearest integer.
console.log("Floor:", Math.floor(3.8));

// Finding the maximum value from a list of numbers.
console.log("Maximum value:", Math.max(3, 4, 2, 8));

// Finding the minimum value from a list of numbers.
console.log("Minimum value:", Math.min(3, 4, 2, 8));

// Accessing the mathematical constant PI.
console.log("PI:", Math.PI);

// The atan2 function returns the arctangent of the quotient of its arguments.
console.log("atan2 function:", Math.atan2);
