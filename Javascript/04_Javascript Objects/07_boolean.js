// Using logical AND (&&) operator to check if both x and y are true.
var x = true;
var y = false;

console.log(x && y);  // Output: false

// Using logical OR (||) operator to check if either x or y is true.
console.log(x || y);  // Output: true 

// Using logical NOT (!) operator to negate the value of x.
console.log(!x);  // Output: false

// Using logical NOT (!) operator to negate the value of y.
console.log(!y);  // Output: true

// Creating a boolean object using the Boolean constructor.
var b = new Boolean(true);
console.log(b.valueOf()); // Output: true
