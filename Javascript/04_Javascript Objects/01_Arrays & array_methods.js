// Array: A collection of objects.

// Declaring an array with various types of elements.
var myArray = [1, "Hello", [2, 3]];

// Printing the length of the array.
console.log("Length of myArray:", myArray.length);

// Push method: Adds an element to the end of the array.
myArray.push("World");
console.log("After push:", myArray);

// Pop method: Removes the last element of the array.
myArray.pop();
console.log("After pop:", myArray);

// Shift method: Removes the first element of the array.
myArray.shift();
console.log("After shift:", myArray);
