// Operators and Expressions

// Declaring variables x, y, and z
let x = 10; // x is assigned the value 10
let y = 20; // y is assigned the value 20

// Adding x and y, and assigning the result to z
let z = x + y; // z is assigned the result of adding x and y, which is 30
console.log("z =", z); // Output: z = 30

// Adding 5 multiplied by 3 to 10, and assigning the result to a
let a = 10 + 5 * 3; // a is assigned the result of the expression 10 + (5 * 3), which is 25
console.log("a =", a); // Output: a = 25

// Adding 10 and 5 first, then multiplying by 3, and assigning the result to b
let b = (10 + 5) * 3; // b is assigned the result of the expression (10 + 5) * 3, which is 45
console.log("b =", b); // Output: b = 45
