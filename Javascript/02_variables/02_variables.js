// In JavaScript, variables are used to store data. 

// 1. Declaring Variables

  // To declare a variable in JavaScript, you use the "var" keyword followed by the name of the variable.

  // For example:

var x;    //This declares a variable called "x" but does not assign any value to it.


var x = 10;  //assign a value to a variable when you declare it, like this:

  // In JavaScript, you can also use the "let" and "const" keywords to declare variables. 


let y = 20;
const z = 30;
  // In this example, "y" is a variable that can be reassigned, while "z" is a constant that cannot be reassigned.


// 2. Variable Naming Rules

    // JavaScript is a dynamically-typed language, which means that you don't have to specify
    // the data type of a variable when you declare it. The data type of a variable is
    // determined by the value that is assigned to it. 

    // For example:

    var a = 10; // a is a number
    var b = "hello"; // b is a string
    var c = [1, 2, 3]; // c is an array